package SkillBuilding;

public class RectanglePerimeter 
{

	public static void main(String[] args) 
	{
		System.out.println("Perimeter of a rectangle is 2w + 2l");		
		int Width=4;
		int Length=13;
		System.out.println("");
		System.out.println("Width of the rectangle is 4");
		System.out.println("");
		System.out.println("Length of the rectangle is 13");
		int perimeter= 2*Width + 2*Length;
		System.out.println("");
		System.out.print("Perimeter of this rectangle is: ");
		System.out.print(  perimeter);		

	}

}